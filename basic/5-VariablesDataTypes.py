
# Variables are nothing but reserved memory locations to store values. 
# This means that when you create a variable you reserve some space in memory.

# Based on the data type of a variable, the interpreter allocates memory 
# and decides what can be stored in the reserved memory. Therefore, 
# by assigning different data types to variables, you can store integers, 
# decimals or characters in these variables.

counter = 100          # An integer assignment
miles   = 1000.0       # A floating point
name    = "John"       # A string

print(counter) 
print(miles)
print(name) 